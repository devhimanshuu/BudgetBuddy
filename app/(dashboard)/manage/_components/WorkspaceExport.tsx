"use client";

import { useState } from "react";
import {
  Card,
  CardContent,
  CardDescription,
  CardHeader,
  CardTitle,
} from "@/components/ui/card";
import { 
  FileDown, 
  FileSpreadsheet, 
  FileText, 
  Loader2, 
  Download,
  ShieldCheck,
  Zap,
  TrendingDown,
  TrendingUp,
  CreditCard
} from "lucide-react";
import { Button } from "@/components/ui/button";
import { toast } from "sonner";
import { mkConfig, generateCsv, download } from "export-to-csv";
import jsPDF from "jspdf";
import autoTable from "jspdf-autotable";
import { format } from "date-fns";
import { PermissionGuard } from "@/components/PermissionGuard";
import { GetFormatterForCurrency } from "@/lib/helper";

export function WorkspaceExport({ workspaceId, workspaceName, currency }: { 
    workspaceId: string, 
    workspaceName: string,
    currency: string 
}) {
  const [isExportingCSV, setIsExportingCSV] = useState(false);
  const [isExportingPDF, setIsExportingPDF] = useState(false);

  const fetchExportData = async () => {
    const res = await fetch(`/api/transactions/search?workspaceId=${workspaceId}&pageSize=5000`);
    if (!res.ok) throw new Error("Failed to fetch data");
    const data = await res.json();
    return data.transactions || [];
  };

  const handleCSVExport = async () => {
    setIsExportingCSV(true);
    try {
      const transactions = await fetchExportData();
      
      const csvConfig = mkConfig({
        fieldSeparator: ",",
        decimalSeparator: ".",
        useKeysAsHeaders: true,
        filename: `${workspaceName}_Export_${format(new Date(), "yyyy-MM-dd")}`,
      });

      // Transform data for CSV
      const exportData = transactions.map((t: any) => ({
        Date: format(new Date(t.date), "yyyy-MM-dd"),
        Description: t.description,
        Category: t.category,
        Type: t.type,
        Amount: t.amount,
        Currency: currency,
        User: t.userId,
        Status: t.status || "Approved"
      }));

      const csv = generateCsv(csvConfig)(exportData);
      download(csvConfig)(csv);
      toast.success("CSV Export generated successfully!");
    } catch (error) {
      console.error(error);
      toast.error("Failed to generate CSV export");
    } finally {
      setIsExportingCSV(false);
    }
  };

  const handlePDFExport = async () => {
    setIsExportingPDF(true);
    try {
      const transactions = await fetchExportData();
      const doc = new jsPDF();
      
      // Header
      doc.setFontSize(22);
      doc.setTextColor(16, 185, 129); // Emerald
      doc.text("BudgetBuddy Professional Report", 14, 22);
      
      doc.setFontSize(10);
      doc.setTextColor(100);
      doc.text(`Workspace: ${workspaceName}`, 14, 30);
      doc.text(`Generated on: ${format(new Date(), "PPP HH:mm")}`, 14, 35);
      doc.text(`Total Records: ${transactions.length}`, 14, 40);

      // Summary Table Logic
      const totals = transactions.reduce((acc: any, t: any) => {
        if (t.type === "income") acc.income += t.amount;
        if (t.type === "expense") acc.expense += t.amount;
        return acc;
      }, { income: 0, expense: 0 });

      const formatter = GetFormatterForCurrency(currency);

      autoTable(doc, {
        startY: 50,
        head: [["Summary", "Amount"]],
        body: [
          ["Total Income", formatter.format(totals.income)],
          ["Total Expenses", formatter.format(totals.expense)],
          ["Net Flow", formatter.format(totals.income - totals.expense)],
        ],
        theme: "striped",
        headStyles: { fillColor: [16, 185, 129] },
      });

      autoTable(doc, {
        startY: (doc as any).lastAutoTable.finalY + 15,
        head: [["Date", "Description", "Category", "Type", "Amount"]],
        body: transactions.map((t: any) => [
          format(new Date(t.date), "MMM d, yyyy"),
          t.description,
          t.category,
          t.type.toUpperCase(),
          formatter.format(t.amount)
        ]),
        columnStyles: {
            4: { halign: 'right' }
        },
        headStyles: { fillColor: [30, 41, 59] }, // Slate-800
      });

      // Footer
      const pageCount = doc.getNumberOfPages();
      for (let i = 1; i <= pageCount; i++) {
        doc.setPage(i);
        doc.setFontSize(8);
        doc.setTextColor(150);
        doc.text(`Page ${i} of ${pageCount}`, doc.internal.pageSize.width / 2, doc.internal.pageSize.height - 10, { align: 'center' });
        doc.text("Generated by BudgetBuddy Pro", 14, doc.internal.pageSize.height - 10);
      }

      doc.save(`${workspaceName}_Tax_Report_${format(new Date(), "yyyy_MM")}.pdf`);
      toast.success("Professional PDF Report generated!");
    } catch (error) {
      console.error(error);
      toast.error("Failed to generate PDF report");
    } finally {
      setIsExportingPDF(false);
    }
  };

  return (
    <PermissionGuard allowedRoles={["ADMIN"]}>
      <Card className="border-primary/20 shadow-xl bg-gradient-to-br from-card to-primary/5">
        <CardHeader>
          <div className="flex items-center justify-between">
            <div className="space-y-1">
              <CardTitle className="flex items-center gap-2 text-primary">
                <FileDown className="w-5 h-5" />
                Workspace Export
              </CardTitle>
              <CardDescription>
                Export your workspace data for tax, accounting, or record keeping.
              </CardDescription>
            </div>
            <div className="p-2 bg-primary/10 rounded-full">
                <Zap className="w-5 h-5 text-primary" />
            </div>
          </div>
        </CardHeader>
        <CardContent className="grid grid-cols-1 md:grid-cols-2 gap-4">
          <div className="group relative flex flex-col items-center justify-center p-4 rounded-2xl border-2 border-dashed border-primary/20 bg-background/50 hover:bg-primary/5 transition-all cursor-pointer"
               onClick={handleCSVExport}>
            <div className="p-3 rounded-full bg-blue-500/10 text-blue-500 mb-2 group-hover:scale-110 transition-transform">
              <FileSpreadsheet className="w-6 h-6" />
            </div>
            <h3 className="font-bold text-base">Detailed CSV</h3>
            <p className="text-xs text-muted-foreground text-center mt-1 max-w-[200px]">
              Raw transaction data formatted for Excel or Sheets.
            </p>
            <Button 
                variant="link" 
                className="mt-2 text-blue-500 h-8 text-xs"
                disabled={isExportingCSV}
            >
              {isExportingCSV ? <Loader2 className="w-3 h-3 animate-spin mr-2" /> : <Download className="w-3 h-3 mr-2" />}
              {isExportingCSV ? "Generating..." : "Download CSV"}
            </Button>
          </div>

          <div className="group relative flex flex-col items-center justify-center p-4 rounded-2xl border-2 border-dashed border-primary/20 bg-background/50 hover:bg-emerald-500/5 transition-all cursor-pointer"
               onClick={handlePDFExport}>
            <div className="p-3 rounded-full bg-emerald-500/10 text-emerald-500 mb-2 group-hover:scale-110 transition-transform">
              <FileText className="w-6 h-6" />
            </div>
            <h3 className="font-bold text-base">Professional PDF</h3>
            <p className="text-xs text-muted-foreground text-center mt-1 max-w-[200px]">
              Branded reports perfect for taxes or reimbursements.
            </p>
            <Button 
                variant="link" 
                className="mt-2 text-emerald-500 h-8 text-xs"
                disabled={isExportingPDF}
            >
              {isExportingPDF ? <Loader2 className="w-3 h-3 animate-spin mr-2" /> : <ShieldCheck className="w-3 h-3 mr-2" />}
              {isExportingPDF ? "Generating..." : "Generate PDF"}
            </Button>
          </div>
        </CardContent>
      </Card>
    </PermissionGuard>
  );
}
